# Notifications System
# ===================
# 
# A comprehensive notification system for the platform that handles:
# - Real-time in-app notifications
# - Email digest notifications  
# - Push notifications (future)
# - User preferences and subscriptions
# - Event aggregation and deduplication
# - Quiet hours and rate limiting
#
# This module provides:
# - Database models for notifications, preferences, and subscriptions
# - Event bus for publishing and processing notification events
# - Delivery mechanisms (in-app, email, push)
# - REST API endpoints for managing notifications
# - WebSocket support for real-time updates
# - React UI components for notification management
